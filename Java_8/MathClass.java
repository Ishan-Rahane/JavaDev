public class MathClass {

    public static void main(String[] args) {

        double x = 25.0;
        double y =5;

        System.out.println("Maximum number-->>"+Math.max(x,y));

        System.out.println("Square root number-->>"+Math.sqrt(y));

        System.out.println("Power of number-->>"+Math.pow(x,y));

        System.out.println("Log number-->>"+Math.log(y));


    }
}
